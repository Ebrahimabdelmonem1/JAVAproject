package coursemanagementsystem;
public class Student  {

    private String name;
    private String Pnum ;
    private int id ;
    //////////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////////////////////
    public Student ()
    {
        this.name = getname() ;
        this.Pnum = getNumber();
        this.id =  getid()    ;

    }

     /////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////

    public void setName(String name) {
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNumber(String Pnum)
    {
        this.Pnum = Pnum;
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    public String getname() {
        return name;
    }

    public int getid() {
        return id;
    }
    public String getNumber() {
        return Pnum;
    }

}
